function declineWordByNumber(number, wordForms) {
    let mod100 = number % 100;
    let mod10 = number % 10;
    if (mod100 >= 11 && mod100 <= 19) {
        return wordForms[2];
    } else if (mod10 === 1) {
        return wordForms[0];
    } else if (mod10 >= 2 && mod10 <= 4) {
        return wordForms[1];
    } else {
        return wordForms[2];
    }
}
function declineSeason(number) {
    var numberStr = number.toString();
  
    if (numberStr.slice(-1) === '1' && numberStr.slice(-2) !== '11') {
      return number + ' сезон';
    }
    else if (numberStr.slice(-1) === '2' || numberStr.slice(-1) === '3' || numberStr.slice(-1) === '4') {
      if (numberStr.slice(-2) !== '12' && numberStr.slice(-2) !== '13' && numberStr.slice(-2) !== '14') {
        return number + ' сезона';
      }
    }
    return number + ' сезонов';
  }
